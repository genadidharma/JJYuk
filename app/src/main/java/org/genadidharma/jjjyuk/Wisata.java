package org.genadidharma.jjjyuk;

public class Wisata {


    //    Deklrasi Variable
    private int ulasan ;
    private double rating;
    private String nama_wisata , deskipsi , jam , alamat ,harga_tiket , foto , protokol , status;


    //    Create Setter and Getter
    public int getUlasan() {
        return ulasan;
    }

    public void setUlasan(int ulasan) {
        this.ulasan = ulasan;
    }

    public double getRating() {
        return rating;
    }

    public void setRating(double rating) {
        this.rating = rating;
    }

    public String getNama_wisata() {
        return nama_wisata;
    }

    public void setNama_wisata(String nama_wisata) {
        this.nama_wisata = nama_wisata;
    }

    public String getDeskipsi() {
        return deskipsi;
    }

    public void setDeskipsi(String deskipsi) {
        this.deskipsi = deskipsi;
    }

    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getHarga_tiket() {
        return harga_tiket;
    }

    public void setHarga_tiket(String harga_tiket) {
        this.harga_tiket = harga_tiket;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public String getProtokol() {
        return protokol;
    }

    public void setProtokol(String protokol) {
        this.protokol = protokol;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }




}
